from flask import Flask, render_template, redirect, url_for
from flask_bootstrap import Bootstrap
from flask_wtf import FlaskForm
from wtforms import StringField, SubmitField, SelectField, URLField
from wtforms.validators import DataRequired, url
import csv
from dotenv import load_dotenv
import os


app = Flask(__name__)
app.config['SECRET_KEY'] = os.getenv('KEY')
Bootstrap(app)


class CafeForm(FlaskForm):
    cafe = StringField('Cafe name', validators=[DataRequired()])
    location = URLField('Cafe Location on Google Maps(URL)', validators=[DataRequired(), url()])
    open = StringField('Opening Time e.g. 8AM', validators=[DataRequired()])
    close = StringField('Closing Time e.g. 5.30 PM', validators=[DataRequired()])
    coffee_rating = SelectField(u'Coffee Rating', choices=['âœ˜', 'â˜•', 'â˜•â˜•', 'â˜•â˜•â˜•', 'â˜•â˜•â˜•â˜•', 'â˜•â˜•â˜•â˜•â˜•'])
    wifi_rating = SelectField(u'Wifi Strength Rating', choices=['âœ˜', 'ğŸ’ª', 'ğŸ’ªğŸ’ª', 'ğŸ’ªğŸ’ªğŸ’ª', 'ğŸ’ªğŸ’ªğŸ’ªğŸ’ª', 'ğŸ’ªğŸ’ªğŸ’ªğŸ’ªğŸ’ª'])
    power_rating = SelectField(u'Power Socket Availability', choices=['âœ˜', 'ğŸ”Œ', 'ğŸ”ŒğŸ”Œ', 'ğŸ”ŒğŸ”ŒğŸ”Œ', 'ğŸ”ŒğŸ”ŒğŸ”ŒğŸ”Œ', 'ğŸ”ŒğŸ”ŒğŸ”ŒğŸ”ŒğŸ”Œ'])
    submit = SubmitField('Submit')


# all Flask routes below
@app.route("/")
def home():
    return render_template("index.html")


@app.route('/add', methods=["GET", "POST"])
def add_cafe():
    form = CafeForm()
    if form.validate_on_submit():
        print("True")
        with open("cafe-data.csv", mode="a", encoding="utf8") as csv_file:
            csv_file.write(f"\n{form.cafe.data},"
                           f"{form.location.data},"
                           f"{form.open.data},"
                           f"{form.close.data},"
                           f"{form.coffee_rating.data},"
                           f"{form.wifi_rating.data},"
                           f"{form.power_rating.data}")
        return redirect(url_for('cafes'))
    return render_template('add.html', form=form)


@app.route('/cafes')
def cafes():
    with open('cafe-data.csv', newline='', encoding="utf8") as csv_file:
        csv_data = csv.reader(csv_file, delimiter=',')
        list_of_rows = []
        for row in csv_data:
            list_of_rows.append(row)
    return render_template('cafes.html', cafes=list_of_rows)


if __name__ == '__main__':
    app.run(debug=True)
